# ###### Environment Variables ######

# =================================
# Environment variables specify the behaviour of the software package installed in Linux
# The path where the packages have been installed will be specified in environment variables
# =================================
# ### Few Commands ###

# ---------------------------------
# To print list of environment variables
printenv
# ---------------------------------
# To print the path of the home directory of the user
echo $HOME
# ---------------------------------
# This command will give a colon separated list of directories in which the shell looks for commands
echo $PATH
# ---------------------------------
# To print the hostname
echo $HOSTNAME
# ---------------------------------
# To print the language being used
echo $LANG
# ---------------------------------
# To print the version of this instance of bash
echo $BASH_VERSION
