# Bash and shell scripting --> they are good for light task automation

# Shell scripting and Bash isn't for writing complex software (it can be done). The place for bash is light task automation. It's for small repetitive tasks that we want to script and reasonably portable so that we can use them again in any Linux/Unix machine we want.

# Use shell scripting where it is appropriate and use other tools where they are appropriate.

# The Bourne shell (sh) is a shell, or command-line interpreter, for computer operating systems.

# Bash is a command processor that typically runs in a text window where the user types commands that cause actions. Bash can also read and execute commands from a file, called a shell script. Like all Unix shells, it supports filename globbing (wildcard matching), piping, here documents, command substitution, variables, and control structures for condition-testing and iteration. The keywords, syntax, dynamically scoped variables and other basic features of the language are all copied from sh.

